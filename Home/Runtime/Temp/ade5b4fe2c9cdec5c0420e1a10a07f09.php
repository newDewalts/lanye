<p style="text-align:center;">
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">医疗保障扶贫三年行动实施方案</span>
</p>
<p style="text-align:center;">
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">(2018-2020年)</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　党的十八大以来，医疗保障立足现有制度，采取综合措施，着力提高农村贫困人口医疗保障水平，在缓解贫困人口因病致贫因病返贫方面发挥了重要作用。党的十九大明确把精准脱贫作为决胜全面建成小康社会的三大攻坚战之一，作出新的部署。为进一步做好建档立卡贫困人口、特困人员等农村贫困人口医疗保障工作，完善医疗保障扶贫顶层设计，进一步明确细化扶贫政策，推动工作有效落实，特制定本实施方案。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　一、总体要求</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　(一)指导思想</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　全面贯彻党的十九大和十九届二中、三中全会精神，以习近平新时代中国特色社会主义思想为指导，认真贯彻落实习近平总书记关于脱贫攻坚的重要指示精神，坚持精准扶贫精准脱贫基本方略，坚持脱贫攻坚目标和现行扶贫标准，将打赢脱贫攻坚战作为当前和今后三年的首要任务，重点聚焦“三区三州”等深度贫困地区和因病致贫返贫等特殊贫困人口，立足当前、着眼长远，精准施策、综合保障，实现参保缴费有资助、待遇支付有倾斜、基本保障有边界、管理服务更高效、就医结算更便捷，充分发挥基本医保、大病保险、医疗救助各项制度作用，切实提高农村贫困人口医疗保障受益水平，为实现2020年我国现行标准下农村贫困人口脱贫提供坚强保障。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　(二)任务目标</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　到2020年，农村贫困人口全部纳入基本医保、大病保险、医疗救助范围，医疗保障受益水平明显提高，基本医疗保障更加有力。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　----实现农村贫困人口制度全覆盖，基本医保、大病保险、医疗救助覆盖率分别达到100%。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　----基本医保待遇政策全面落实，保障水平整体提升，城乡差距逐步均衡。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　----大病保险加大倾斜力度，农村贫困人口大病保险起付线降低50%、支付比例提高5个百分点、逐步提高并取消封顶线。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　----医疗救助托底保障能力进一步增强，确保年度救助限额内农村贫困人口政策范围内个人自付住院医疗费用救助比例不低于70%，对特殊困难的进一步加大倾斜救助力度。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　----促进定点医疗机构严格控制医疗服务成本，减轻农村贫困人口目录外个人费用负担。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　----医疗保障经办管理服务不断优化，医疗费用结算更加便捷。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　(三)基本原则</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　坚持现有制度，加强综合保障。立足基本医保、大病保险、医疗救助现有制度功能，坚持普惠政策与特惠措施相结合，统筹医疗保障扶贫整体设计，合理统筹使用资金和服务资源，充分发挥综合保障合力。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　坚持基本保障，明确责任边界。严格执行基本医疗保障支付范围和标准，加强医疗费用管控、提高资金使用效率，尽力而为、量力而行，千方百计保基本、始终做到可持续，防止不切实际过高承诺、过度保障，避免造成基金不可持续和出现待遇“悬崖效应”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　坚持精准扶贫，确保扶贫实效。精准识别扶贫对象，精准使用扶贫资金，精准实施扶贫政策，加强贫困人口精细化管理，掌握贫困底数，细化扶贫措施，明确扶贫目标，落实各级责任，夯实扶贫效果。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　坚持协同配合，形成保障合力。发挥机构改革优势，加强制度政策协同;加强医疗保障扶贫与医疗扶贫衔接，协同解决深度贫困地区医疗资源不足问题，提高贫困人口医疗服务利用可及性;坚持社会保障与家庭尽责相结合，既加大外部帮扶，又引导增强自我健康意识，落实家庭照护责任。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　二、重点措施</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　(一)完善可持续筹资政策，实现贫困人口应保尽保</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　1.稳步提高城乡居民医保筹资水平和医疗救助政府补助水平。合理提高城乡居民医保政府补助标准和个人缴费标准。省级财政要加大对深度贫困地区倾斜力度，按照规定足额安排补助资金并及时拨付到位。加大对城乡医疗救助的投入，2018年起中央财政连续三年通过医疗救助资金渠道安排补助资金，用于提高深度贫困地区农村贫困人口医疗保障水平，加强医疗救助托底保障。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　2.将农村建档立卡贫困人口作为医疗救助对象，实现农村贫困人口基本医保、大病保险和医疗救助全覆盖，其中对特困人员参保缴费给予全额补贴、对农村建档立卡贫困人口给予定额补贴，逐步将资助参保资金统一通过医疗救助渠道解决。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　3.结合全民参保计划的推进，探索建立适合农村贫困人口特点的参保办法，提升经办服务能力，做好身份标识、组织参保和信息采集等工作，加强信息共享和数据比对，配合有关部门做好农村贫困人口参保缴费工作，确保已核准有效身份信息的农村贫困人口全部参保，实现应保尽保。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　(二)实施综合保障措施，提高贫困人口待遇水平</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　4.公平普惠提高城乡居民基本医保待遇。全面推进城乡居民基本医保制度整合，均衡城乡保障待遇，稳定住院保障水平。进一步完善城乡居民医保门诊统筹，逐步提高门诊保障水平，扩大门诊保障范围，减轻患者门诊医疗费用负担。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　5.加大大病保险倾斜支付力度。2018年城乡居民医保人均新增财政补助40元的一半(20元)用于大病保险。大病保险支付比例达到50%以上。在此基础上，重点聚焦深度贫困地区和特殊贫困人口，巩固完善大病保险倾斜支付政策。对包括农村贫困人口在内的困难群众降低起付线50%、提高报销比例5个百分点，逐步提高并取消封顶线。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　6.加大医疗救助托底保障力度。完善重特大疾病医疗救助政策，分类分档细化农村贫困人口救助方案，确保年度救助限额内农村贫困人口政策范围内个人自付住院医疗费用救助比例不低于70%;有条件的地区，可在确保医疗救助资金运行平稳情况下，合理提高年度救助限额。在此基础上，对个人及家庭自付医疗费用负担仍然较重的，进一步加大救助力度，并适当拓展救助范围。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　(三)使用适宜技术，促进就医公平可及</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　7.落实基本医疗保障范围规定。全面执行国家基本医保药品目录，将国家医保目录谈判准入药品纳入医保支付范围。落实国家对诊疗项目目录和医疗康复项目的管理要求。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　8.引导落实分级诊疗制度。结合分级诊疗制度建设，将符合规定的家庭医生签约服务费纳入医保支付范围，引导参保人员优先到基层首诊。对于按规定转诊的贫困患者，住院费用可连续计算起付线，省域内就医结算执行所在统筹地区同等支付政策。鼓励有条件的地区将互联网诊疗服务纳入医保支付范围。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　(四)优化基层公共服务，全面推进费用直接结算</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　9.提高深度贫困地区基层医保经办管理服务能力，指定专门窗口和专人负责政策宣传并帮助贫困人口兑现政策，解决群众政策不知情、就医报销难等问题。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　10.全面推进贫困人口医疗费用直接结算。结合城乡居民医保制度整合，推进城乡居民医保、大病保险、医疗救助信息共享和服务衔接，实现农村贫困人口市(地)域范围内“一站式服务、一窗口办理、一单制结算”，减少农村贫困人口跑腿垫资。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　11.做好跨地区就医结算服务。对异地安置和异地转诊的农村贫困人口，医保经办机构要优先做好异地就医登记备案和就医结算等服务，切实做好贫困地区外出就业创业人员异地就医备案工作。2018年率先实现深度贫困地区每个县有一家医院纳入全国跨省异地就医直接结算系统，加快实现深度贫困地区乡镇医院纳入全国跨省异地就医直接结算系统。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　(五)加强医疗服务管理，控制医疗费用不合理增长</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　12.完善支付方式改革，探索建立区域内医疗卫生资源总量、医疗费用总量与经济发展水平、医保基金支付能力相适应的调控机制。深度贫困地区要更加注重医疗费用成本控制，提供使用适宜的基本医疗服务，切实降低农村贫困人口医疗费用总体负担。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　13.完善定点医药机构服务协议管理，健全定点服务考核评价体系，将考核结果与医保基金支出挂钩。全面开展医保智能监控，不断完善医保信息系统，提高医保基金使用效率。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　三、保障措施</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　(一)加强组织领导</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　各级医疗保障部门要把打赢脱贫攻坚战作为重大政治任务，坚持中央统筹、省负总责、市县抓落实的工作机制，强化一把手负总责的领导责任制，明确责任、尽锐出战、狠抓实效。要将医疗保障扶贫工作纳入年度重点任务推进，积极会同扶贫、民政等部门明确农村贫困人口的具体范围，结合实际制订扶贫三年行动具体实施方案，建立医疗保障扶贫工作沟通联系机制，确保各项扶贫政策落实落地。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　(二)坚持现行制度基本标准，狠抓贯彻落实</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　各地要充分认识医疗保障扶贫任务的重要性、艰巨性和长期性，将思想和认识统一到中央的决策部署上来，既要狠抓落实确保扶贫任务全面完成，也要高度重视防范出现不切实际过高承诺、过度保障、不可持续的问题。坚持基本医疗保障标准，充分发挥现有医疗保障制度功能;贯彻落实精准方略，创新医疗保障扶贫机制;坚持严格管理，确保基金长期平稳可持续。对出现的苗头性、倾向性问题，要采取有效措施，及时规范整改，并做好衔接和平稳过渡。各地在现有医保制度之外自行开展的新的医疗保障扶贫措施探索，要在2020年底前转为在基本医保、大病保险和医疗救助三重保障框架下进行。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　(三)建立专项工作调度机制</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　做好农村贫困人口身份标识，建立贫困人口专项管理台账。统筹基本医保、大病保险、医疗救助三项制度，加强农村贫困人口参保缴费、患病就医、待遇保障、费用结算等情况监测。建立医疗保障扶贫专项工作调度机制，按市、省、国家3级定期汇总报送数据，加强医疗保障扶贫工作督导检查。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　(四)深入开展医疗保障扶贫作风专项治理</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　将作风建设贯穿医疗保障扶贫全过程，重点解决贯彻中央脱贫攻坚决策部署不坚决、扶贫责任落实不到位、政策措施不精准、资金管理使用不规范、工作作风不扎实、考核评估不严格等问题。防止形式主义、官僚主义，加强工作实效，切实减轻基层工作负担。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　(五)加强典型宣传和风险防范</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　深入宣传习近平总书记关于扶贫工作的重要论述和党中央关于精准扶贫精准脱贫的重大决策部署，宣传医疗保障扶贫成就和典型事迹，营造良好的舆论氛围。加强医疗保障扶贫政策风险评估，建立重大事件应急处置机制，加强对脱贫攻坚的舆情监测，合理引导社会舆论。</span>
</p>
