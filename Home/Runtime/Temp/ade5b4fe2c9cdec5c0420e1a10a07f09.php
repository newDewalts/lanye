	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　吉林长春长生公司问题疫苗案件发生后，习近平作出重要指示，强调以猛药去疴、刮骨疗毒的决心，完善我国疫苗管理体制，坚决守住安全底线，全力保障群众切身利益和社会安全稳定大局。李克强作出重要批示，要求对监管失职渎职行为坚决严厉问责，尽早还人民群众一个安全、放心、可信任的生活环境。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　10月22日下午，十三届全国人大常委会第六次会议在京召开，本次会议对药品管理法修正草案进行了一审，同日对基本医疗卫生与健康促进法草案进行了二审。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　药品管理法修正草案一审 实行疫苗责任强制保险制度</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　“党中央、国务院要求汲取教训，举一反三，抓紧完善相关法律法规，加快完善疫苗药品监管长效机制。”22日下午，国家药品监督管理局局长焦红作了关于药品管理法修正草案的说明。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　“围绕问题疫苗案件暴露的突出问题、实施药品上市许可持有人制度和推进审批制度改革等进行修改，及时回应社会关切，对其他不太急需的内容待下一步全面修订时再作修改。”焦红表示。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　疫苗研制应留存追溯信息</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　北京青年报记者注意到，修正草案完善了药品全过程监管制度。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　“强化企业主体责任，要求药品上市许可持有人、生产经营企业的法定代表人或者主要负责人对药品的质量和生产经营活动全面负责。”焦红说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　她说，强化药品生产经营过程管理，要求生产经营过程必须持续符合法定要求，并补充药品原辅料供应商审核、出厂检验、上市审核等制度，严把原辅料采购、出厂、上市等关口。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　值得一提的是，修正草案还明确了药品质量安全追溯要求。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　修正草案新增规定，药品的上市许可持有人、生产企业、经营企业、医疗机构应当建立并实施严格的药品质量安全追溯制度，保证药品可追溯，“在疫苗的研制、生产、流通、预防接种过程中应当采用信息化手段采集、留存追溯信息。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　按照修正草案，国家实行药品安全信息统一公布制度。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　国家药品安全总体情况、药品安全风险警示信息、重大药品安全事故及其调查处理信息和国务院确定需要统一公布的其他信息由国务院药品监督管理部门统一公布。药品安全风险警示信息和重大药品安全事故及其调查处理信息的影响限于特定区域的，也可以由有关省、自治区、直辖市人民政府药品监督管理部门公布，“任何单位和个人不得编造、散布虚假药品安全信息。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　药品有质量问题立即召回</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　在药品召回制度方面，修正案草案新增规定，上市药品存在质量问题或者其他安全隐患的，药品上市许可持有人应当立即停止生产、销售，告知相关生产企业、经营企业、医疗机构停止生产、经营和使用，召回已上市销售的药品，及时公开召回信息，并将药品召回和处理情况向省、自治区、直辖市政府药品监督管理部门和卫生行政部门报告。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　“药品上市许可持有人依法应当召回药品而未实施召回的，省、自治区、直辖市人民政府药品监督管理部门责令其召回。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　如果责令召回但仍拒不召回怎么处理?</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　“拒不召回的，处应召回药品货值金额五倍以上十倍以下的罚款;货值金额不足一万元的，处五万元以上十万元以下的罚款;情节严重的，撤销药品批准证明文件，吊销《药品生产许可证》、《药品经营许可证》。药品生产企业、药品经营企业、医疗机构拒不配合召回的，处二万元以上十万元以下的罚款。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　麻醉药品等不得委托生产</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　值得一提的是，修正草案还强化了对疫苗等特殊药品的监管。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　修正草案新增规定，药品上市许可持有人自行生产药品的，应当取得《药品生产许可证》;委托生产的，应当选择符合条件的药品生产企业。疫苗、血液制品、麻醉药品、精神药品、医疗用毒性药品不得委托生产;但是，国务院药品监督管理部门规定可以委托生产的情形除外。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　“药品上市许可持有人自行经营药品的，应当具备本法规定的药品经营的条件;委托经营的，应当选择符合条件的药品经营企业。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　修正草案明确，国家实行疫苗责任强制保险制度。疫苗的上市许可持有人应当按照国家有关规定投保。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　焦红在作修正草案说明时表示，“要求药品监管部门对药品上市许可持有人、生产经营企业实施药品生产经营质量管理规范的情况进行检查，监督其持续符合要求。必要时可以对为药品研制、生产、经营、使用提供产品或者服务的单位和个人进行延伸检查，对疫苗等生物制品实施重点监督检查。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　药品监督管理部门建立药品的上市许可持有人、生产企业、经营企业、医疗机构药品安全信用档案，记录许可颁发、日常监督检查结果、违法行为查处等情况依法向社会公布并实时更新。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　“对有不良信用记录的，增加监督检查频次;对违法行为情节严重的，按规定实施联合惩戒。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　草案拟增设责任约谈制度</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　增设责任约谈制度也是一大亮点。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　修正草案还新增规定，药品监督管理部门未及时发现药品安全系统性风险，未及时消除监督管理区域内药品安全隐患的，本级政府或上级政府药品监督管理部门对其主要负责人进行责任约谈。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　地方政府未履行药品安全职责，未及时消除区域性重大药品安全隐患的，上级政府或上级政府药品监督管理部门对其主要负责人进行责任约谈。“被约谈的部门、地方人民政府应当立即采取措施，对药品监督管理工作进行整改。责任约谈情况和整改情况应当纳入地方人民政府和有关部门药品监督管理工作评议、考核记录。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　“细化并加重对地方政府负责人和监管人员的处分。”焦红表示。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　修正草案新增规定，县级以上地方政府存在三种特定行为的，对直接负责的主管人员和其他直接责任人员给予记过或者记大过处分，情节严重的，给予降级、撤职或者开除处分。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　这三种特定行为是：履行职责不力，造成严重不良影响或者重大损失的;隐瞒、谎报、缓报药品安全事故的;本行政区域内发生特别重大药品安全事故，或者连续发生重大药品安全事故的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　此外，本次修正草案还新增规定，药品监督管理部门存在“对发现的药品安全违法行为未及时查处”等三种行为的，对直接负责的主管人员和其他直接责任人员给予记过或者记大过处分，情节较重的，给予降级或者撤职处分;情节严重的，给予开除处分。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　建立药品职业化检查员队伍</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　“要加大对违法行为的处罚力度，解决违法成本低、处罚力度弱的问题。”焦红说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　修正草案明确，国家要建立药品职业化检查员队伍。检查员应当具备药品法律法规和专业知识。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　此外还增加规定，药品监督管理部门应当公布本部门的电子邮件地址、电话，接受咨询、投诉、举报，并依法及时答复、核实、处理。对查证属实的举报，按照有关规定给予举报人奖励。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　北青报记者注意到，修正草案全面加大了对违法行为的行政处罚力度，提高了对违法行为罚款的下限或者上限。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　“例如规定对未经许可生产经营药品的，罚款的幅度从货值金额的二倍至五倍提高到五倍至三十倍;对生产销售假药等违法行为增设‘停产停业’等处罚;明确对生产销售属于假药、劣药的疫苗等6类违法行为，在法定幅度内从严处罚。”焦红在作草案说明时表示。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　修正草案的另一个亮点是落实“处罚到人”要求，对严重违法行为的责任人进行处罚。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　有生产销售假劣药、违反质量管理规范等行为的，对单位的法定代表人或者主要负责人、直接负责的主管人员和其他直接责任人员处以没收收入、罚款、十年直至终身禁业的处罚。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　实施上市许可持有人制度</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　“总结试点经验，全面实施药品上市许可持有人制度，明确上市许可持有人对药品的安全、有效负责，对药品的研制、生产、经营、使用全过程依法承担责任。”焦红在作草案说明时表示。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　她说，修正草案要求在审批药品时，同时审查药品的安全性、有效性以及申请人的质量管理、风险防控和责任赔偿能力。规定药品上市许可持有人具备条件的可以自行生产经营药品，也可以委托符合条件的企业生产经营药品。“药品上市许可持有人对已上市药品的安全性、有效性开展再评价;制定风险管控计划，定期报告药品生产销售、上市后研究、风险管理等情况。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　药品上市许可持有人未制定药品风险管控计划或者未提交年度报告的，责令限期改正;逾期不改正的，处十万元以上二十万元以下的罚款。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　当天下午，焦红还作了《关于延长授权国务院在部分地方开展药品上市许可持有人制度试点期限的决定(草案)》的说明。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　2015年11月4日，第十二届全国人大常委会第十七次会议授权国务院在北京等十个省、直辖市开展药品上市许可持有人制度试点，试点期限为三年，至2018年11月4日结束。焦红介绍，2017年10月，中办、国办印发《关于深化审评审批制度改革鼓励药品医疗器械创新的意见》，提出要及时总结药品上市许可持有人制度试点经验，推动修订药品管理法，力争早日在全国推开。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　“市场监管总局、药监局认真总结试点经验，研究起草了《药品管理法(修正草案)》，已经国务院同意，提请全国人大常委会审议。”焦红说，考虑到药品管理法修正草案内容较多，审议需要一定时间，为做好试点工作和修法工作的衔接，需要将药品上市许可持有人制度试点期限延长至修改完善后的《药品管理法》施行之日。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　基本医疗卫生与健康促进法草案二审</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　禁止任何组织和个人威胁、危害医生人身安全</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　基本医疗卫生与健康促进法草案二审稿新增规定，药品生产企业、药品经营企业和医疗卫生机构应当对所生产、经营、使用的药品质量、疗效和反应进行监测。因预防接种异常反应造成受种者死亡、严重残疾或者器官组织损伤的，应当依法给予补偿。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　根据二审稿，任何组织或者个人不得扰乱医疗卫生机构执业场所秩序。医疗卫生人员的人身安全、人格尊严不受侵犯。禁止任何组织和个人威胁、危害医疗卫生人员人身安全，侵犯医疗卫生人员人格尊严。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　造成死亡严重残疾应补偿</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　北青报记者注意到，根据党中央关于完善疫苗药品管理法律法规和制度规则的精神，针对长春长生问题疫苗案件暴露的突出问题，草案二审稿新增了不少内容。</span>
</p>
<p>
