</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　《通知》紧紧围绕扶贫任务，提出了医疗保障扶贫的六大目标。一是实现农村贫困人口制度全覆盖，基本医保、大病保险、医疗救助覆盖率均达到100%。二是全面落实基本医保待遇政策，整体提升保障水平，逐步均衡城乡差距。三是加大大病保险倾斜力度，对农村贫困人口降低起付线50%、提高支付比例5个百分点、逐步提高并取消封顶线。四是进一步增强医疗救助托底保障能力，确保年度救助限额内农村贫困人口政策范围内个人自付住院医疗费用救助比例不低于70%，对特殊困难的进一步加大倾斜救助力度。五是使用适宜技术，促进定点医疗机构严格控制医疗服务成本，减轻农村贫困人口目录外个人费用负担。六是不断优化医疗保障经办管理服务，全面推进贫困人口医疗费用直接结算。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　为全面落实上述目标，《通知》从贯穿医保参保缴费、待遇支付、保障标准、管理服务、就医结算等全过程，提出了5个方面具体措施：一是完善可持续筹资政策，加大医疗救助财政投入，落实分类资助农村贫困人口参保缴费政策，实现贫困人口应保尽保。二是实施综合保障措施，全面推进城乡居民基本医保制度整合，增加大病保险筹资并巩固完善倾斜力度，完善重特大疾病医疗救助，对个人负担仍然较重的贫困人口进一步加大救助力度，提高贫困人口待遇水平。三是全面执行国家基本医保药品目录、诊疗项目目录和医疗康复项目管理要求，将国家医保目录谈判准入药品纳入医保支付范围，将符合规定的家庭医生签约服务费纳入医保支付范围，对按规定转诊的贫困患者，省域内就医结算执行所在统筹地区同等支付政策。四是全面推进贫困人口医疗费用直接结算，实现市(地)域范围内城乡居民医保、大病保险、医疗救助“一站式服务、一窗口办理、一单制结算”;优先做好异地安置、异地转诊和外出就业创业人员异地就医备案和结算工作。五是加强医疗服务管理，完善支付方式改革，提供使用适宜的基本医疗服务，切实降低贫困人口医疗费用总体负担;完善定点医药机构服务协议管理，全面开展医保智能监控，提高医保基金使用效率。</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　为确保各项扶贫措施全面落地，《通知》明确各级医疗保障部门加强组织领导，强化一把手负总责的领导责任制，尽锐出战、狠抓落实。深入开展医保扶贫作风专项治理，既要狠抓各项扶贫政策的贯彻落实，也要防范和及时纠正不切实际过高承诺、过度保障、不可持续的问题，坚持基本标准，贯彻精准方略，坚持严格管理，确保扶贫任务全面完成。</span>
