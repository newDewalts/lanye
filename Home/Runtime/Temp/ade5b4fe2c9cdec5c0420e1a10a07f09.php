	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　取消药品加成后，广东在医药分开上又有新举措!</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　患者在医院看完病拿到处方，手机付费后，无需在医院等拿药，可到家附近的药店取药，或者药品快递到家。10月25日，广东省第二人民医院，在全省率先开通处方流转平台服务，第一张流转处方同步开出。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　10月25日，广东省第二人民医院全省率先开通处方流转平台服务，患者在医院看病拿到医生处方后，可选在院外药店购买或配送到家，少去排队拿药之苦，也改变了过去拿着医院处方在外买不到药的窘境。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　医院方面表示，目前该服务仅适用于自费患者，医保政策对接上以后，将为医保患者开通该服务功能。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　44岁的欧先生家住海珠，平时经常喝酒、饮食不规则，五年前出现胃疼，饱胀不适，在医院胃镜检查是慢性浅表性胃炎，在省二医看病开药。以往经常高峰时期，他等拿药一般要等半个小时。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　10月25日，他尝到了处方流转平台服务的“头啖汤”。看病结束拿到医生处方后，他通过微信线上缴费，选择家附近的药店取药，无需在医院等拿药了。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　这是广东省第二人民医院处方流转平台上线后开出的一张处方，也是全省首家实现处方流转的三甲大型医院。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　医院内外药品同规同价 处方外流落地</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　在上线仪式上，广东省第二人民医院党委书记、院长田军章介绍，通过处方流转平台外流出去的是处方信息，以往病人看病需在医院完成就诊、开具处方、获得药品等步骤，现在医院让处方自由流动，药品由社会药店提供，就诊和药品进一步分离。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　省二医方面介绍，医生开出处方(西药处方)，自费患者通过线上缴费，点击进入到处方流转平台，平台提示让患者到附近有资质的药店取药，或者选择配送到家。选择完成后，患者在微信上会收到取药号，可到药店凭号取药或者收取相应药品的快递件。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　据介绍，这种模式与目前一些医院推出的院外药房不同。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　在院外药房模式中，患者是拿着处方到指定药房取药。而在省二医的处方流转平台模式里，患者的处方是流转到平台上，截至目前，平台已签约全省21374家药店，其中在广州市就有3834家药店供患者选择。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　实际上，关于处方外流的呼声早已有之，但是患者拿着医院的处方，在药店往往依旧买不到药，因为医院的药品与药店的药品品规不同，价格不同。以某品牌抗病毒口服液为例，医院的药是18支一盒，药店是14支一盒，致使外流处方难以落地。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　田军章院长介绍，该院的处方流转平台实现医院信息系统(HlS系统)与药店管理系统(ERP系统)无缝对接，统一了药品目录，解决拿着医院处方在药店买不到药品的尴尬，实现医院处方真正外流，患者可以真正买到处方里的药。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　田军章院长特别强调，药店配送的所有药品与医院配发的药品同规格、同厂家、同价格，在药店里有药师核实药品，患者无须担心院外药品的差价和质量问题。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　医院方面特别提醒，由于目前处方外流服务仍处于起步阶段，因此只配送口服西药，未含针剂，链接到平台的药店将逐步铺开此项业务。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　此外，值得注意的是，处方在外流之前已实现脱敏，也就是去掉病史、检查信息等只保留用药信息，保护患者隐私信息安全。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　处方外流 医患双赢</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　对于医院来说，处方外流有着同样的正向作用。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　田军章院长告诉广卫君，药品已实现了零差率，对于医院来说，药品采购管理也是一笔成本。因此，处方外流在方便老百姓取药的同时，也降低了医院药品采购、管理等成本。在英美等国家的医院早已采用药品外流的方式配发药品。“目前，省二医平均每天的处方量约6000张，自费病人的处方大概占了50%。”</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　田军章院长认为，大型三甲医院未来门诊量将下降，药房规模将会缩小，大医院应把更多精力放在危急重症患者的处理上。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　广东省第二人民医院党委副书记、副院长李观明告诉广卫君，“处方外流可让医院药师腾出时间，更好地做好住院患者的药品管理、临床用药指导和药事管理等工作，并进行临床药学、临床药理等更高层次的研究。”</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　该平台是开放平台，欢迎其他医院加入。“在未来，为实现医药分家，降低医院药品管理成本，相信会有更多医院使用处方流转平台配发药品。同时，平台建设越来越完善，医生只管开处方，不选择厂家、价格，开好处方后有多种药品可以选择，患者可选择不同厂家和不同价格的药品，就近采购，自由度更大，患者选择更多。”田军章说。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　处方外流 真正医药分家</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　医药分开是医改中最难啃的骨头，也是一直努力的方向。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　自去年7月15日，广东公立医院全面取消药品加成后，老百姓购药成本明显下降，这次实现处方流转无疑又是深化医改的一大进步。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　有业内人士认为，如果说实现药品零差率之时，医和药仍然紧密相连，那么处方流转平台上线后，医和药之间千丝万缕的联系则进一步被斩断，有望解决药品流通环节中深层次的问题，实现医改的题中之义。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　近年来国家一直在努力推进处方外流。据资料显示，从2014年的探索“患者凭处方到医疗机构或零售药店自主购药新模式”对患者的鼓励，到2015年的“禁止医院限制处方外流”对医院的推动，到2017年医改任务中“探索医疗机构处方信息、医保结算信息与药品零售消费信息互联互通、实时共享”清扫院外处方流转的系统性障碍。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;line-height:2.5;">　　今年6月14日，广东省人民政府公布《促进“互联网+医疗健康”发展行动计划(2018-2020年)》，就处方外流的细则做出规划。“行动计划”指出，推广处方流转平台，发展配送中心，支持医院、药品生产流通企业、药店、符合条件的第三方机构共同参与处方流转、药品物流配送。推广智慧药房，鼓励医院处方外配、信息共享，改造传统药品保障流程，为患者提供“一站式”药事服务。</span> 
</p>
